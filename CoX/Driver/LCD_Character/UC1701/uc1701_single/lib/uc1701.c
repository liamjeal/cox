//*****************************************************************************
//
//! \brief Init the UC1701 LCD.
//!
//! \param ulSpiClock SPI clock to select.
//!
//! This function initialize the mcu SPI as master and specified SPI port.
//!
//! \return None.
//
//*****************************************************************************
void UC1701Init(unsigned long ulSpiClock)
{
    //
    // Enable the GPIOx port which is connected with UC1701
    //
    xSysCtlPeripheralEnable(xGPIOSPinToPort(LCD_PIN_SPI_CLK));
    xSysCtlPeripheralEnable(xGPIOSPinToPort(LCD_PIN_SPI_CS));
    xSysCtlPeripheralEnable(xGPIOSPinToPort(LCD_PIN_SPI_MOSI));
    xSysCtlPeripheralEnable(xGPIOSPinToPort(LCD_PIN_CD));
    xSysCtlPeripheralEnable(xGPIOSPinToPort(LCD_PIN_RESET));
    
    //
    // Enable the SPIx which is connected with UC1701
    //
    xSysCtlPeripheralEnable2(LCD_PIN_SPI_PORT);
    xSysCtlPeripheralReset2(LCD_PIN_SPI_PORT);
    //
    // Set the chip select pin as OUT_MODE
    //
    xGPIOSPinDirModeSet(LCD_PIN_SPI_CS, xGPIO_DIR_MODE_OUT);
    //
    // Set the chip reset pin as OUT_MODE
    //
    xGPIOSPinDirModeSet(LCD_PIN_CD, xGPIO_DIR_MODE_OUT);
    xGPIOSPinDirModeSet(LCD_PIN_RESET, xGPIO_DIR_MODE_OUT);
    
    xSPinTypeSPI(SPI_CLK, LCD_PIN_SPI_CLK);
    xSPinTypeSPI(SPI_MOSI, LCD_PIN_SPI_MOSI);
    
    //
    // Configure MCU as a master device , 8 bits data width ,MSB first,Mode_0
    //
    xSPIConfigSet(LCD_PIN_SPI_PORT, ulSpiClock, xSPI_MOTO_FORMAT_MODE_1 |
                                                xSPI_MODE_MASTER | 
                                                xSPI_MSB_FIRST |
                                                xSPI_DATA_WIDTH8);
    //
    // Disable UC1701 when Power up
    //
    xGPIOSPinWrite(LCD_PIN_SPI_CS, 1);

    //
    // Reset the chip
    //
    xGPIOSPinWrite(LCD_PIN_RESET, 0);
    delay_ms(2);
    xGPIOSPinWrite(LCD_PIN_RESET, 1);
    delay_ms(6);

    //
    // Initial Configuration
    //
    UC1701CmdWrite(0xe2);//System Reset
	UC1701CmdWrite(0x2c);//Power Rise Step1
	delay_ms(8);
	UC1701CmdWrite(0x2e);//Power Rise Step2
	delay_ms(8);
	UC1701CmdWrite(0x2f);//Power Rise Step3
	delay_ms(8);
	UC1701CmdWrite(0x23);
	UC1701DoubleCmdWrite(0x81, 0x1f);//Set LCD resistor ratio

	UC1701CmdWrite(0xa2);//Bias 1/9
	UC1701CmdWrite(0xc0);//Set COM Direction
	UC1701CmdWrite(0xa0);//Set SEG Direction
	UC1701CmdWrite(0x40);//Set Scroll Line: the first line
	UC1701CmdWrite(0xaf);//Display Enable

}


//*****************************************************************************
//
//! \brief Send a data to the device.
//!
//! \param ucData The data to send.
//!
//! This function is to send a data to the device.
//!
//! \return None.
//
//*****************************************************************************
void UC1701DataWrite(unsigned char ucData)
{
    unsigned long ulTemp; 

    //
    //Step 1 Select Chip
    //
    xGPIOSPinWrite(LCD_PIN_SPI_CS, 0);
    
    //
    //Step 2 Set Display Enable
    //
    ulTemp = ucData;
    xGPIOSPinWrite(LCD_PIN_CD, 1);
    xSPIDataWrite(LCD_PIN_SPI_PORT, &ulTemp, 1);
    
    //
    //Step 3 Disable chip select
    // 
    xGPIOSPinWrite(LCD_PIN_SPI_CS, 1);
}

//*****************************************************************************
//
//! \brief Send a command to the device.
//!
//! \param ucCmd The command to send.
//!
//! This function is to send a command to the device.
//!
//! \return None.
//
//*****************************************************************************
void UC1701CmdWrite(unsigned char ucCmd)
{
    //
    //Step 1 Select Chip
    //
    xGPIOSPinWrite(LCD_PIN_SPI_CS, 0);
    
    //
    //Step 2 Send a command
    //
    xGPIOSPinWrite(LCD_PIN_CD, 0);
    xSPIDataWrite(LCD_PIN_SPI_PORT, &ucCmd, 1);
    
    //
    //Step 3 Disable chip select
    // 
    xGPIOSPinWrite(LCD_PIN_SPI_CS, 1);
}

//*****************************************************************************
//
//! \brief Send a double-byte command to the device.
//!
//! \param ucCmd The command to send.
//! \param ucData The data to send.
//!
//! This function is to send a double-byte command to the device.
//!
//! \return None.
//
//*****************************************************************************
void UC1701DoubleCmdWrite(unsigned char ucCmd, unsigned char ucData)
{
    //
    //Step 1 Select Chip
    //
    xGPIOSPinWrite(LCD_PIN_SPI_CS, 0);
    
    //
    //Step 2 Set command
    //
    xGPIOSPinWrite(LCD_PIN_CD, 0);
    xSPIDataWrite(LCD_PIN_SPI_PORT, &ucCmd, 1);
    xSPIDataWrite(LCD_PIN_SPI_PORT, &ucData, 1);
    
    //
    //Step 3 Disable chip select
    // 
    xGPIOSPinWrite(LCD_PIN_SPI_CS, 1); 
}

//*****************************************************************************
//
//! \brief Set the start address.
//!
//! \param ucPA The line of the data write in.
//! \param ucCA The address of the data write in.
//!
//! This function is to set the start address.
//!
//! \return None.
//
//*****************************************************************************
void UC1701AddressSet(unsigned char ucPA, unsigned char ucCA)
{
  
    //
    //Step 1 Set Page Address
    //
    UC1701CmdWrite((ucPA & UC1701_SET_PA_MASK) | UC1701_SET_PA);
    
    //
    //Step 2 Set Column Address MSB
    //
    UC1701CmdWrite(((ucCA>>4) & UC1701_SET_CA_MASK) | UC1701_SET_CA_MSB);

    //
    //Step 3 Set Column Address LSB
    //
    UC1701CmdWrite(((ucCA) & UC1701_SET_CA_MASK) | UC1701_SET_CA_LSB);
}


//*****************************************************************************
//
//! \brief Send the data to the address.
//!
//! \param ucPA The line of the char show on.
//! \param ucCA The position of the line.
//! \param ucAsciiWord The word's ascii data.
//!
//! This function is to send the data to the address.
//!
//! \return None.
//
//*****************************************************************************
void UC1701Dispaly(unsigned char ucLine, unsigned char ucRow,
                   unsigned char ucAsciiWord)
{
    int i=0,k=0;
    unsigned char uctemp;
    k=(ucAsciiWord-32)*16;
    
    for(i=0;i<8;i++)
    {
        UC1701AddressSet((ucLine*2),(127-(ucRow*8)-i));
        uctemp=Ascii[k+i];
        UC1701DataWrite(uctemp);
    }  
    for(i=0;i<8;i++)
    {
        UC1701AddressSet((ucLine*2)+1,(127-(ucRow*8)-i));
        uctemp=Ascii[k+i+8];
        UC1701DataWrite(uctemp);
    }  
}

//*****************************************************************************
//
//! \brief inverse display a char.
//!
//! \param ucPA The line of the char show on.
//! \param ucCA The position of the line.
//! \param ucAsciiWord The word's ascii data.
//!
//! This function is to inverse display a char.
//!
//! \return None.
//
//*****************************************************************************
void UC1701InverseDispaly(unsigned char ucLine, unsigned char ucRow,
                          unsigned char ucAsciiWord)
{
    int i=0,k=0;
    unsigned char uctemp;
    k=(ucAsciiWord-32)*16;
    
    for(i=0;i<8;i++)
    {
        UC1701AddressSet((ucLine*2),(127-(ucRow*8)-i));
        uctemp=~Ascii[k+i];
        UC1701DataWrite(uctemp);
    }  
    for(i=0;i<8;i++)
    {
        UC1701AddressSet((ucLine*2)+1,(127-(ucRow*8)-i));
        uctemp=~Ascii[k+i+8];
        UC1701DataWrite(uctemp);
    } 
}

//*****************************************************************************
//
//! \brief show the words on LCD.
//!
//! \param ucPA The line of the char show on.
//! \param ucCA The position of the line.
//! \param pcChar the pointer of the words.
//!
//! This function is to show the words on LCD.
//!
//! \return None.
//
//*****************************************************************************
void UC1701CharDispaly(unsigned char ucLine, unsigned char ucRow,
                       char *pcChar)
{
    xASSERT((ucLine <= 3) && (ucRow <= 15));
    do
    {
        UC1701Dispaly(ucLine,ucRow,*pcChar++);
        ucRow++;
        if(ucRow>15)
        {
            ucLine++;
            ucRow=0;
            if(ucLine>3)
                break;
        }
    }
    while(*pcChar!='\0');  
}

//*****************************************************************************
//
//! \brief show the Chinese on LCD.
//!
//! \param ucPA The line of the char show on.
//! \param ucCA The position of the line.
//! \param ucLength The length of the Chinese.
//! \param pcChar the pointer of the words.
//!
//! This function is to show the Chinese on LCD.
//!
//! \return None.
//
//*****************************************************************************
void UC1701ChineseDispaly(unsigned char ucLine, unsigned char ucRow,
                          unsigned char ucLength, char *pcChar)
{
    int i,j=0;
    xASSERT((ucLine <= 3) && (ucRow <= 15));
    
    for(j=0;j<ucLength;j++)
    {
        for(i=0;i<16;i++)
        {
            UC1701AddressSet((ucLine*2),(127-(ucRow*8)-i));
            UC1701DataWrite(pcChar[j*32+i]);
        }  
        for(i=0;i<16;i++)
        {
            UC1701AddressSet((ucLine*2)+1,(127-(ucRow*8)-i));
            UC1701DataWrite(pcChar[j*32+16+i]);
        }  
        ucRow = ucRow +2;
        if (ucRow > 14)
        {
            ucLine++;
            if(ucLine > 3)
            {
                break;
            }
        }
    }    
}
//*****************************************************************************
//
//! \brief Display a decimal number on the LCD.
//!
//! \param n is the number that to display.
//!
//! Display a decimal number on the cursor location.
//!
//! \return None.
//
//*****************************************************************************
void UC1701DisplayN(unsigned char ucLine, unsigned char ucRow,
                     unsigned long n)
{
    char pcBuf[16], *p;

    if (n == 0)
    {
        UC1701Dispaly(ucLine, ucRow, '0');
    }
    else
    {
        p = pcBuf;
        while (n != 0)
        {
            *p++ = (n % 10) + '0';
            n /= 10;
        }

        while (p > pcBuf)
        {
            UC1701Dispaly(ucLine, ucRow, *--p);
            ucRow++;
            if (ucRow > 14)
            {
                ucLine++;
                if(ucLine > 3)
                {
                    break;
                }
            }
        }
    }
}

//*****************************************************************************
//
//! \brief clear all pannal.
//!
//! \param None
//!
//! This function is to clear all pannal.
//!
//! \return None.
//
//*****************************************************************************
void UC1701Clear(void)
{
	unsigned char i,j;

	for(i=0;i<9;i++)
	{
		xGPIOSPinWrite(LCD_PIN_SPI_CS, 0);
		UC1701CmdWrite(0xb0+i);
		UC1701CmdWrite(0x10);
		UC1701CmdWrite(0x00);
		for(j=0;j<132;j++)
		{
			UC1701DataWrite(0x00);
		}
	}
}

